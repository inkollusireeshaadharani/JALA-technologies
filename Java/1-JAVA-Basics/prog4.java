/*Define variables for different Data Types int, boolean, char, float, double and print on the
 console
*/
class prog4{
	
	public static void main(String args[]) {
		
		int a=10;
		boolean b=false;
		char c='C';
		float d=9.2f;
		double e=8.90;
		
		System.out.println("value of 'int' a = "+a);
		System.out.println("value of 'boolean' b = "+b);
		System.out.println("value of 'char' c = "+c);
		System.out.println("value of 'float' d = "+d);
		System.out.println("value of 'double' e = "+e);
	}
}